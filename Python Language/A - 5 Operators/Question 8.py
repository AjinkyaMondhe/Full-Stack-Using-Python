#Write a python script to use IN operator to display the data present in the list


#------------ IN, NOT IN Are the Membership Operators ------------
# Which Check if a substring or Element is present in the String or List respectively.

string = "My Name is Ajinkya"
print('My' in string) #True
print('my' in string) #False

list = [1,2,3,4,5]
print(3 in list) #True
print(7 in list) #False